function updateLanguage(lang) {
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            // Handle placeholders
            if (element.tagName === 'INPUT' && element.getAttribute('placeholder')) {
                element.placeholder = translations[lang][key];
                return;
            }

            // Handle HTML content (for line breaks etc)
            // If the element has children (like icons), we need to be careful.
            // Strategy: If it has an icon, look for the text node or span.
            // Ideally, we should wrap text in spans in the HTML.
            // For now, if we detect an icon, we try to replace the text part.

            if (element.querySelector('i')) {
                // It has an icon. Let's assume the text is after the icon.
                // This is risky. Better to ensure HTML has text wrapped.
                // But if we use innerHTML, we can reconstruct it? No, translation doesn't have icon.
                // Let's assume the data-i18n is on the wrapper, and we want to keep the icon.
                // Actually, in my plan, I put data-i18n on the <span> or <p> that contains the text, NOT the parent with the icon.
                // So element.innerHTML = translation is safe if I follow that rule.
                element.innerHTML = translations[lang][key];
            } else {
                element.innerHTML = translations[lang][key];
            }
        }
    });

    // Update Toggle UI
    const toggles = document.querySelectorAll('.lang-toggle-btn');
    toggles.forEach(btn => {
        const btnLang = btn.getAttribute('data-lang');
        if (btnLang === lang) {
            btn.classList.add('active');
            btn.classList.remove('btn-outline-light', 'btn-outline-primary');
            btn.classList.add('btn-light', 'text-primary'); // Aesthetic active state
        } else {
            btn.classList.remove('active', 'btn-light', 'text-primary');
            btn.classList.add('btn-outline-light'); // Inactive state
        }
    });

    // Sync specific mobile/desktop classes if needed
    // For the pill design, we'll use a specific structure.
    document.querySelectorAll('.lang-switch-pill').forEach(pill => {
        pill.querySelectorAll('span').forEach(span => {
            if (span.getAttribute('data-lang') === lang) {
                span.classList.add('active');
            } else {
                span.classList.remove('active');
            }
        });
    });
}

let currentLang = 'id';

function toggleLanguage(lang) {
    if (lang) {
        currentLang = lang;
    } else {
        currentLang = currentLang === 'id' ? 'en' : 'id';
    }
    updateLanguage(currentLang);
}

// Initialize
document.addEventListener('DOMContentLoaded', function () {
    updateLanguage(currentLang);

    // Bind click events to new pill toggles
    document.querySelectorAll('.lang-switch-pill span').forEach(span => {
        span.addEventListener('click', function () {
            toggleLanguage(this.getAttribute('data-lang'));
        });
    });
});
